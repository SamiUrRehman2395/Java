public class Sum {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;
        int num4 = 40;

        // Calculate the sum
        int sum = num1 + num2 + num3 + num4;

        // Print the sum
        System.out.println("The sum of the four numbers is: " + sum);
    }
}
